#include "libft.h"

int		ft_issort(int *tab, int length, int (*f)(int, int))
{
	int i;

	i = -1;
	if (!tab)
		return (0);
	while (++i < length - 1)
		if (f(tab[i], tab[i + 1]) > 0)
			return (0);
	return (1);
}
